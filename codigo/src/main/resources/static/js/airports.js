// Função para remover acentos de uma string
function removeAccents(str) {
    return str.normalize('NFD').replace(/[\u0300-\u036f]/g, '');
}

// Função para buscar aeroportos que correspondem à query
function searchAirports(query) {
    if (!query || query.length < 2) {
        return [];
    }
    
    const searchTerm = removeAccents(query.toLowerCase());
    
    return airports.filter(airport => {
        return removeAccents(airport.iata.toLowerCase()).includes(searchTerm) ||
               removeAccents(airport.city.toLowerCase()).includes(searchTerm) ||
               removeAccents(airport.name.toLowerCase()).includes(searchTerm) ||
               removeAccents(airport.country.toLowerCase()).includes(searchTerm);
    }).slice(0, 10); // Limita a 10 resultados
}

// Função para formatar o resultado do autocomplete
function formatAirport(airport) {
    return `${airport.iata} - ${airport.city}, ${airport.country}`;
}

